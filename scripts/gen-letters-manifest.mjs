// Scans public/letters/ and writes lib/lettersManifest.ts — the list of
// cut-out glyph files that actually exist, base first then numbered variants.
// Run after dropping new letter PNGs:  npm run letters:manifest
// Also runs automatically before every build (see package.json "prebuild").

import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const lettersDir = join(root, "public", "letters");
const outFile = join(root, "lib", "lettersManifest.ts");

// match "<char>.png" or "<char>-<n>.png" where char is a single a-z or 0-9
const RE = /^([a-z0-9])(?:-(\d+))?\.png$/;

const byChar = new Map(); // char -> Array<{ variant: number, file: string }>

for (const name of readdirSync(lettersDir)) {
  const m = RE.exec(name);
  if (!m) continue; // skips README.txt, "home (1).png", etc.
  const char = m[1];
  // base file has no number -> sort key -1 so it always comes first
  const variant = m[2] === undefined ? -1 : Number(m[2]);
  if (!byChar.has(char)) byChar.set(char, []);
  byChar.get(char).push({ variant, file: `/letters/${name}` });
}

const manifest = {};
for (const [char, list] of [...byChar.entries()].sort()) {
  list.sort((a, b) => a.variant - b.variant);
  manifest[char] = list.map((x) => x.file);
}

const body = `// AUTO-GENERATED by scripts/gen-letters-manifest.mjs — do not edit by hand.
// Run \`npm run letters:manifest\` after adding files to public/letters/.

export const LETTERS: Record<string, string[]> = ${JSON.stringify(manifest, null, 2)};
`;

writeFileSync(outFile, body);
const total = Object.values(manifest).reduce((n, a) => n + a.length, 0);
console.log(`letters manifest: ${Object.keys(manifest).length} chars, ${total} files -> lib/lettersManifest.ts`);
