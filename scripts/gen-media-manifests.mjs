// Scans public/gallery/ and public/yearbook-pages/ at BUILD TIME and writes
// lib/galleryManifest.ts + lib/yearbookManifest.ts. This keeps the (very large)
// image folders out of the serverless bundle: runtime code reads only these
// small generated manifests, so Next's file tracer never pulls the ~300MB of
// photos into a Vercel function — the images stay static CDN assets.
//
// Runs automatically before every build (package.json "prebuild").
// Re-run manually after adding/removing photos:  npm run media:manifest

import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import exifr from "exifr";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const HEADER =
  "// AUTO-GENERATED by scripts/gen-media-manifests.mjs — do not edit by hand.\n" +
  "// Regenerate with `npm run media:manifest` (also runs before every build).\n\n";

// ── gallery: photos tagged with a capture time, oldest → recent ──────────────
const galleryDir = join(root, "public", "gallery");

/**
 * Pulls a capture timestamp (ms epoch) out of a filename. Handles the common
 * phone/camera patterns: 20240311_100654, IMG_20240311_100654, 2024-03-11, etc.
 * Returns null when no plausible date is embedded.
 */
function dateFromFilename(name) {
  const m = name.match(
    /(20\d{2})[-_.]?(\d{2})[-_.]?(\d{2})(?:[-_.T]?(\d{2})[-_.]?(\d{2})[-_.]?(\d{2}))?/
  );
  if (!m) return null;
  const [, y, mo, d, hh = "12", mm = "00", ss = "00"] = m;
  const year = +y,
    month = +mo,
    day = +d;
  if (month < 1 || month > 12 || day < 1 || day > 31) return null;
  const t = new Date(year, month - 1, day, +hh, +mm, +ss).getTime();
  return Number.isNaN(t) ? null : t;
}

async function buildGallery() {
  let files;
  try {
    files = readdirSync(galleryDir);
  } catch {
    return [];
  }
  const imgs = files.filter((f) => /\.(png|jpe?g|webp|avif)$/i.test(f));

  const photos = await Promise.all(
    imgs.map(async (f) => {
      const full = join(galleryDir, f);
      const src = `/gallery/${f}`;

      // 1. a timestamp baked into the filename is the most reliable
      const fromName = dateFromFilename(f);
      if (fromName !== null) return { src, time: fromName, dated: true };

      // 2. EXIF capture date — read bytes ourselves and hand exifr a Buffer
      try {
        const buf = readFileSync(full);
        const ex = await exifr.parse(buf, {
          pick: ["DateTimeOriginal", "CreateDate"],
        });
        const d = ex?.DateTimeOriginal ?? ex?.CreateDate;
        if (d instanceof Date && !Number.isNaN(d.getTime())) {
          return { src, time: d.getTime(), dated: true };
        }
      } catch {
        /* no readable EXIF — fall through */
      }

      // 3. last resort: file modified time (flagged approximate)
      let mtime = 0;
      try {
        mtime = statSync(full).mtimeMs;
      } catch {
        /* leave at 0 */
      }
      return { src, time: mtime, dated: false };
    })
  );

  return photos.sort((a, b) => a.time - b.time);
}

// ── yearbook flipbook page exports (1.png, 2.png, …), numeric sort ───────────
const pagesDir = join(root, "public", "yearbook-pages");

function buildYearbookPages() {
  let files;
  try {
    files = readdirSync(pagesDir);
  } catch {
    return [];
  }
  return files
    .filter((f) => /\.(png|jpe?g|webp)$/i.test(f))
    .sort((a, b) =>
      a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" })
    )
    .map((f) => `/yearbook-pages/${f}`);
}

const gallery = await buildGallery();
const pages = buildYearbookPages();

writeFileSync(
  join(root, "lib", "galleryManifest.ts"),
  HEADER +
    'import type { GalleryPhoto } from "@/types";\n\n' +
    `export const GALLERY_PHOTOS: GalleryPhoto[] = ${JSON.stringify(
      gallery,
      null,
      2
    )};\n`
);

writeFileSync(
  join(root, "lib", "yearbookManifest.ts"),
  HEADER +
    `export const YEARBOOK_PAGES: string[] = ${JSON.stringify(pages, null, 2)};\n`
);

console.log(
  `[media-manifest] gallery: ${gallery.length} photos · yearbook: ${pages.length} pages`
);
